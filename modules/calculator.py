def add(a, b):
	'''takes two numeric values and return addition of those two numbers.'''
	return a + b

def sub(a, b):
	'''takes two numeric values and return substraction of those two numbers.'''
	return a - b

def mul(a, b):
	'''takes two numeric values and return multiplication of those two numbers.'''
	return a * b

def div(a, b):
	'''takes two numeric values and return division of those two numbers.'''
	return a / b